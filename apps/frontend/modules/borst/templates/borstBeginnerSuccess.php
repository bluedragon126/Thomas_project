                    <b>”Börstjänaren är bäst!”</b>
                    En av våra användare, en egenföretagare i restaurangbranschen, berättade för oss att Börstjänaren var den första börssajt han kom till som inte helt ung men väl grön börsspekulant. &rdquo;Vad snackar dom för goja&rdquo;, tänkte han (enligt vad han senare berättade), då vår approach inte alls stämde med hans bild av och förväntningar på börsen. Han vandrade vidare mot andra sajter, grönare gräs och jaktmarker. Han hamnade till slut på webbplatser i USA, där dom ju naturligtvis, enligt vad han trodde, måste begripa allting så mycket bättre. </p>
